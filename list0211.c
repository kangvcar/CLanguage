/*
	读取三个整数，并显示出它们的合计值和平均值
*/

#include <stdio.h>

int main(void)
{
	int a, b, c;
	int sum;
	double ave;
	
	puts("请输入三个整数。");
	printf("整数a：");	scanf("%d",&a);
	printf("整数b：");	scanf("%d",&b);
	printf("整数c：");	scanf("%d",&c); 
	
	sum = a+b+c;
	ave = (a+b+c)/3;
	
	printf("它们的合计值是%5d\n", sum);
	printf("它们的平均值是%5.1f\n", ave);
	
	return 0;
} 