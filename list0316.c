/*
	计算所输入的两个整数中的较大的数和较小的数并显示
*/

#include <stdio.h>

int main(void)
{
	int n1, n2, max, min;
	
	puts("请输入两个整数：");
	printf("整数1：");	scanf("%d",&n1);
	printf("整数2：");	scanf("%d",&n2);
	
	if(n1 > n2){
		max = n1;
		min = n2;
	}else{
		max = n2;
		min = n1;
	}
	
	printf("这两个整数中，\n较大的是%d\n较小的是%d\n", max, min);
	
	return 0;
} 